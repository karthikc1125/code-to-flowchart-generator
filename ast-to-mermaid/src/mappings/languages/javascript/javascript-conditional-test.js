let x = 10;

// Simple if statement
if (x > 5) {
  let result = "Greater than 5";
}

// If-else statement
if (x > 15) {
  let result = "Greater than 15";
} else {
  let result = "Not greater than 15";
}

// If-else-if chain
if (x > 20) {
  let result = "Greater than 20";
} else if (x > 15) {
  let result = "Between 15 and 20";
} else if (x > 5) {
  let result = "Between 5 and 15";
} else {
  let result = "Not greater than 5";
}