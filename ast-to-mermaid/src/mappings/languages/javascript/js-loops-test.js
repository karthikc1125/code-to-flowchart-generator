// JavaScript loops test
for (let i = 0; i < 3; i++) {
    console.log("For loop iteration: " + i);
}

let j = 0;
while (j < 2) {
    console.log("While loop iteration: " + j);
    j++;
}

let k = 0;
do {
    console.log("Do-while loop iteration: " + k);
    k++;
} while (k < 2);

console.log("End of loops");